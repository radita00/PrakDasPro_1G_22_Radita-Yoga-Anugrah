package AlsdPraktikum14;

public class Customer21 {
    public int id;
    public String name;

    public Customer21(){

    }

    public Customer21(int id, String name){
        this.id = id;
        this.name = name;
    }

    public String toString(){
        return "ID: " + this.id + " Nama: " + this.name;
    }
}
